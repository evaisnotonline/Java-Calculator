package com.qa.helloworld;

public class Operators {

	public static int add(int a, int b) {
		int sum = a + b;
		return sum;
	}

	public static int times(int a, int b) {
		int times = a * b;
		return times;
	}

	public static int subtract(int a, int b) {
		int subtract = a - b;
		return subtract;
	}

	public static double divide(double a, double b) {
		double divide = a / b;
		return divide;
	}
}
